function drawCells() {
    
    for (let row = 0; row < population.length; row++) {
            
      for (let col = 0; col < population[row].length; col++) {
          let x = col * size 
          let y = row * size
          const cell = population[row][col]
          ctx.fillStyle = cell === 1 ? "black" : "white";
          ctx.fillRect(x, y, size, size);
      }
    }  
}





